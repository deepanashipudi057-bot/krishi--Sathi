function changeLanguage() {
    const selectedLang = document.getElementById('languageSelect').value;
    localStorage.setItem('language', selectedLang);
    updatePageContent(selectedLang);
    
    // Reload data if on a category page
    if (typeof loadWeatherData === 'function') loadWeatherData();
    if (typeof loadSeedsCropsData === 'function') loadSeedsCropsData();
    if (typeof loadPesticidesData === 'function') loadPesticidesData();
    if (typeof loadSchemesData === 'function') loadSchemesData();
}

function updatePageContent(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(el => {
        const key = el.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });
    
    // Update specific elements
    const welcomeText = document.getElementById('welcomeText');
    const subtitleText = document.getElementById('subtitleText');
    
    if (welcomeText) welcomeText.textContent = translations[lang].welcomeText;
    if (subtitleText) subtitleText.textContent = translations[lang].subtitleText;
}

// Load saved language on page load
window.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('language') || 'en';
    document.getElementById('languageSelect').value = savedLang;
    updatePageContent(savedLang);
});
